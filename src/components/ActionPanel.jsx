const actions = [
  { id: "visual", label: "Visualization" },
  { id: "fullscreen", label: "Fullscreen" },
  { id: "sound", label: "Sound" },
  { id: "menu", label: "Menu" },
];

export default function ActionPanel({ onAction, disabled, soundEnabled = true, expanded = false, visualMode = false }) {
  return (
    <aside className="action-panel menu-top">
      {actions.map((item) => {
        const className = `action-button menu-top__control${item.id === "sound" && !soundEnabled ? " muted" : ""}${item.id === "fullscreen" && expanded ? " active" : ""}`;
        return (
          <button
            key={item.id}
            className={className}
            data-action={item.id}
            type="button"
            disabled={disabled}
            onClick={() => onAction(item.id)}
            title={item.id === "fullscreen" && expanded ? "Exit fullscreen view" : item.label}
          >
            {item.id === "visual" && (visualMode ? <EyeClosedIcon /> : <EyeOpenIcon />)}
            {item.id === "fullscreen" && (expanded ? <EnterFullscreenIcon /> : <ExitFullscreenIcon />)}
            {item.id === "sound" && (soundEnabled ? <SoundIcon /> : <SoundOffIcon />)}
            {item.id === "menu" && <BurgerIcon />}
          </button>
        );
      })}
    </aside>
  );
}
